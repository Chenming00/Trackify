export const I18nContext = createContext();

export function I18nProvider({ children }) {
  // auto-detect system language or load from cache
  const getInitialLang = () => {
    const saved = localStorage.getItem('trackify_lang');
    if (saved && dictionaries[saved]) return saved;
    const sys = navigator.language;
    if (sys.startsWith('zh-TW') || sys.startsWith('zh-HK')) return 'zh-TW';
    if (sys.startsWith('zh')) return 'zh-CN';
    if (sys.startsWith('ja')) return 'ja';
    return 'en';
  };

  const [lang, setLang] = useState(getInitialLang);

  useEffect(() => {
    localStorage.setItem('trackify_lang', lang);
    document.documentElement.lang = lang;
    document.title = t('title');
  }, [lang]);

  const t = (key) => dictionaries[lang]?.[key] || dictionaries['zh-CN'][key] || key;

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useTranslation() {
  return useContext(I18nContext);
}
